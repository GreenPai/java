package chap1;

public class VariableInitializationExample {
    public static void main(String[] args) {
        // value 선언
        int value = 0;
        
        // 연산 결과를 변수 result의 초기갑스로 대입
        int result = value + 10;
        
        //변수 result 값을 읽고 콘솔에 출력
        System.out.println(result);
    }
}
